int ft_strncmp(char *s1, char *s2, unsigned int n);
{
	unsigned int z;
	z = 0;
	if(n == 0)
		return(0);
	while(s1[z] == s2[z] && s1[z] != '\0' && s2[z] != '\0' && z < n - 1)
		z++;
	return(s1[z] -s2[z]);
}
